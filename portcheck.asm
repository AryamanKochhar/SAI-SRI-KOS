MOV TMOD, #20H
MOV TH1, #-6
MOV TCON, #50H
SETB TR1
CHECK:
JB P1.7, YES
MOV DPTR, #MSG1
NO:
CLR A
MOVC A, @A + DPTR
JZ CHECK
ACALL LOAD
INC DPTR
SJMP NO
YES:
MOV DPTR, #MSG2
YO:
CLR A
MOVC A, @A + DPTR
JZ CHECK
ACALL LOAD
INC DPTR
SJMP YO

LOAD:
MOV SBUF, A
HERE: JNB TI, HERE
CLR TI
RET

MSG2: DB "YES", 0
MSG1: DB "NO", 0
END
